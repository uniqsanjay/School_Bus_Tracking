package com.example.school_bus_tracking.com.example.school_bus_tracking.modelview;

public class StringValues {

    private String stud_name;
    private String course;
    private String year;
    private String college;
    private String address;
    private String parent_name;
    private String parent_mob;
    private String password;
    
    public String getStud_name() {
        return stud_name;
    }

    public void setStud_name(String stud_name) {
        this.stud_name = stud_name;
    }

    public String getCourse() {
        return course;
    }

    public void setCourse(String course) {
        this.course = course;
    }

    public String getYear() {
        return year;
    }

    public void setYear(String year) {
        this.year = year;
    }

    public String getCollege() {
        return college;
    }

    public void setCollege(String college) {
        this.college = college;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getParent_name() {
        return parent_name;
    }

    public void setParent_name(String parent_name) {
        this.parent_name = parent_name;
    }

    public String getParent_mob() {
        return parent_mob;
    }

    public void setParent_mob(String parent_mob) {
        this.parent_mob = parent_mob;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }
}
